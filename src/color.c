#include "wolf.h"

void		tile_color(int wt, t_win *cr)
{
	if (wt == 1)
		cr->objcl = 0;
	else if (wt == 2)
		cr->objcl = 0;
	else if (wt == 3)
		cr->objcl = 0;
}
